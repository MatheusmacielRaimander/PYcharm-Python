n= input('Digite algo: ')
print('O tipo primitivo desse valor e',type(n))
print('So tem espaços? ',n.isspace())
print('É um numero? ',n.isnumeric())
print('É um alfabético? ',n.isalpha())
print('É alfanumérico? ',n.isalnum())
print('Esta em maiúsculas? ',n.isupper())
print('Esta em minúsculas? ',n.islower())
print('Esta capitalizada? ',n.istitle())